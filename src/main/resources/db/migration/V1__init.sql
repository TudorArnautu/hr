
  CREATE TABLE ANGAJATI (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	nume varchar(255) not null,
	prenume varchar(255) not null,
	id_functie int not null
);

  CREATE TABLE CONSULTATII_MEDICALE (
  id INT GENERATED BY DEFAULT AS IDENTITY,
  data varchar(255) not null,
  id_angajat INT not null
  );

  CREATE TABLE CURSANTI (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	numar_cursanti INT not null,
	nivel_cunostinte varchar(255) not null
);

  CREATE TABLE CURSURI (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	nume_curs varchar(255) not null
);

  CREATE TABLE CV (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	adresa varchar(255) not null,
	telefon int not null,
	email varchar(255) not null,
	nationalitate varchar(255) not null,
	data_nastere varchar(255) not null,
	sex varchar(255) not null,
	oras varchar(255) not null,
	tara varchar(255) not null,
	nume varchar(255) not null,
	prenume varchar(255) not null
);

  CREATE TABLE DEPARTAMENTE (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	nume varchar(255) not null
);

  CREATE TABLE EDUCATIE (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	descriere varchar(255) not null,
	id_cv INT not null,
	an_absolvire int not null,
	facultate varchar(255) not null,
	ani_facultate INT not null
);

  CREATE TABLE EXPERIENTA (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	descriere varchar(255) not null,
	id_cv int not null,
	experienta_anterioara varchar(255) not null,
	angajator varchar(255) not null,
	pozitii_ocupate varchar(255) not null,
	ani_experienta int not null,
	limbi_vorbite varchar(255) not null
);

  CREATE TABLE FUNCTII (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	nume_functie varchar(255) not null,
	salariu int not null,
	id_departament int not null
);

  CREATE TABLE LOGIN (
	id_angajat INT GENERATED BY DEFAULT AS IDENTITY,
	nume_utilizator varchar(255) not null,
	parola varchar(255) not null
);

  CREATE TABLE PLANIFICARE_CONCEDII (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	tip_concediu varchar(255) not null,
	data VARCHAR(255) not null,
	id_angajat int not null
);

  CREATE TABLE PROGRAMARE_CURSURI (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	data VARCHAR(255) not null,
	id_cursanti int not null
);

